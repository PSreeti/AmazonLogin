package Conditions;

public class Switch {
    public static void main(String[] args) {

        int RollNumber = 1;
        switch (RollNumber) {
            case 1:
                System.out.println("RollNumber Number 1");
                break;

            case 2:
                System.out.println("RollNumber Number 2");
                break;

            case 3:
                System.out.println("RollNumber Number 3");
                break;
            case 4:
                System.out.println("RollNumber Number 4");
                break;
        }

    }}
